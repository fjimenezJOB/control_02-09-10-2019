# Aqui estan los metodos con las operaciones de la calculadora

class Calculadora:

    numero01 = 0
    numero02 = 0

    def __init__(self):
        self.numero01 = numero01
        self.numero02 = numero02

    def sumar(self, numero01, numero02):
        resultado = numero01 + numero02
        return resultado

    def restar(self, numero01, numero02):
        resultado = numero01 - numero02
        return resultado

    def multiplicar(self, numero01, numero02):
        resultado = numero01 * numero02
        return resultado

    def dividir(self, numero01, numero02):
        resultado = numero01 / numero02
        return resultado
